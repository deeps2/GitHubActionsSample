package com.shikhar.githubworkflowsample;

public class Foo {

    public int add2Num(int x, int y) {
        return x + y;
    }

    public int difference2Num(int x, int y) {
        return x - y;
    }

    public int multiply2Num(int x, int y) {
        return x * y * y;
    }

    public int divide2Num(int x, int y) {
        return x/y;
    }
}
